#include <stdio.h>


int sum(int a,int b)
{
    int sum;
    sum=a+b;
    return sum;
}
int diff(int a,int b)
{
    int diff;
    diff=a-b;
    return diff;
}
int prod(int a,int b)
{
    int prod;
    prod=a*b;
    return prod;
}
float div(int a,int b)
{
    float div;
    div=(float)a/(float)b;
    return div;  
}