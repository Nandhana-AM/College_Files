public class currency {
    
    public double usd_to_inr(double m)
    {
        return m*83.72;
    }
    public double inr_to_usd(double m)
    {
        return m/83.72;
    }
    public double euro_to_inr(double m)
    {
        return m*90.86;
    }
    public double inr_to_euro(double m)
    {
        return m/90.86;
    }
    public double yen_to_inr(double m)
    {
        return m*0.54;
    }
    public double inr_to_yen(double m)
    {
        return m/0.54;
    }
    
}
