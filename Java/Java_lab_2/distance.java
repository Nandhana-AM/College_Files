 public class distance {
    
    public double m_to_km(double d)
    {
        return d/1000;
    }
    public double km_to_m(double d)
    {
        return d*1000;
    }
    public double miles_to_km(double d)
    {
        return d*1.6;
    }
    public double km_to_miles(double d)
    {
        return d/1.6;
    }
}
