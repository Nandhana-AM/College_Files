public class time {
    
    public double hrs_to_mins(double t)
    {
        return t*60;
    }
    public double mins_to_hrs(double t)
    {
        return t/60;
    }
    public double mins_to_secs(double t)
    {
        return t*60;
    }
    public double secs_to_mins(double t)
    {
        return t/60;
    }

}
